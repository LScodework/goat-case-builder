function setUpPlayerSelector(selectID){

    const playerSelect = document.getElementById(selectID);

    const playerPanel = playerSelect.closest(".player-panel");

    const playerResult = playerPanel.querySelector(".player-result");

    const playerPhoto = playerResult.querySelector(".player-photo");

    const playerName = playerResult.querySelector(".player-summary h3");

    const physicalDetails = playerResult.querySelector(".physical-value");

    const teamsValue = playerResult.querySelector(".teams-value");

    const eraValue = playerResult.querySelector(".era-value");

    const awardNumbers = playerResult.querySelectorAll(".award-number");

    const mvpYearsGrid = playerResult.querySelector(
        '.flip-card[data-award="mvp"] .award-years-grid'
    );

    const finalsMvpYearsGrid = playerResult.querySelector(
        '.flip-card[data-award="finalsMvp"] .award-years-grid'
    );

    const championshipRunsGrid = playerResult.querySelector(
        '.flip-card[data-award="championships"] .award-years-grid'
    );

    const careerList = playerResult.querySelector(".career-accomplishments");

    const goatList = playerResult.querySelector(".goat-arguments");

    playerSelect.addEventListener("change", function(){

        if(playerSelect.value === ""){
            playerResult.hidden = true;
        }
        else{
            const selectedPlayer = players[playerSelect.value];

            if(!selectedPlayer){
                playerResult.hidden = true;
                return;
            }

            playerPhoto.src = selectedPlayer.image;
            playerPhoto.alt = selectedPlayer.name;

            playerName.textContent = selectedPlayer.name;

            physicalDetails.textContent = selectedPlayer.physical;

            teamsValue.innerHTML = "";

            selectedPlayer.teams.forEach((team, index) => {
                const teamEntry = document.createElement("span");
                teamEntry.classList.add("team-entry");
            
                const teamLogo = document.createElement("img");
                teamLogo.src = team.logo;
                teamLogo.alt = `${team.abbreviation} logo`;
                teamLogo.classList.add("team-logo");

                const teamTooltip = document.createElement("span");
                teamTooltip.classList.add("team-tooltip");
                teamTooltip.textContent = team.name; 
            
                const teamText = document.createElement("span");
                teamText.textContent = `${team.abbreviation} ${team.numbers}`;
            
                teamEntry.appendChild(teamLogo);
                teamEntry.appendChild(teamTooltip);
                teamEntry.appendChild(teamText);
            
                teamsValue.appendChild(teamEntry);
            
                if (index < selectedPlayer.teams.length - 1) {
                    const separator = document.createElement("span");
                    separator.classList.add("team-separator");
                    separator.textContent = "·";
                    teamsValue.appendChild(separator);
                }
            });
        
             eraValue.textContent = selectedPlayer.era;

            const awardValues = selectedPlayer.awards.match(/\d+/g);

            if(awardNumbers.length === 3 && awardValues){
                awardNumbers[0].textContent = awardValues[0];
                awardNumbers[1].textContent = awardValues[1];
                awardNumbers[2].textContent = awardValues[2];
            }
            else if(playerDetails[2]){
                playerDetails[2].textContent = selectedPlayer.awards;
            }

            mvpYearsGrid.innerHTML = "";

            if (selectedPlayer.awardDetails) {
            selectedPlayer.awardDetails.mvp.forEach(function(year) {
            const yearItem = document.createElement("span");

            yearItem.classList.add("award-year");
            yearItem.textContent = year;

             mvpYearsGrid.appendChild(yearItem);
             });
            }

            finalsMvpYearsGrid.innerHTML = "";

            if (selectedPlayer.awardDetails) {
                selectedPlayer.awardDetails.finalsMvp.forEach(function(year) {
                    const yearItem = document.createElement("span");
            
                    yearItem.classList.add("award-year");
                    yearItem.textContent = year;
            
                    finalsMvpYearsGrid.appendChild(yearItem);
                });
            
                if (selectedPlayer.awardDetails.finalsMvpNote) {
                    const noteItem = document.createElement("p");
            
                    noteItem.classList.add("award-note");
                    noteItem.textContent = selectedPlayer.awardDetails.finalsMvpNote;
            
                    finalsMvpYearsGrid.appendChild(noteItem);
                }
            }

            championshipRunsGrid.innerHTML = "";

            if (selectedPlayer.awardDetails) {
            selectedPlayer.awardDetails.championships.forEach(function(run) {
            const runItem = document.createElement("div");

        runItem.classList.add("championship-run");

        runItem.innerHTML = `
        <span class="championship-year">${run.year}</span>
        <span class="championship-team">${run.team}</span>
        <span class="championship-opponent">–${run.opponent}</span>
        `;

        championshipRunsGrid.appendChild(runItem);
    });
}

            careerList.innerHTML = "";
            goatList.innerHTML = "";

            selectedPlayer.resume.forEach(function(accomplishment){
                const listItem = document.createElement("li");
                listItem.textContent = accomplishment;
                careerList.appendChild(listItem);
            });

            selectedPlayer.goatArguments.forEach(function(argument){
                const listItem = document.createElement("li");
                listItem.textContent = argument;
                goatList.appendChild(listItem);
            });

            playerResult.hidden = false;
        }
        
        updateAvailablePlayers();
        syncCareerResumeRows();
    });
}
function updateAvailablePlayers() {
    const playerOneSelect = document.getElementById("player-one-select");
    const playerTwoSelect = document.getElementById("player-two-select");

    const playerOneValue = playerOneSelect.value;
    const playerTwoValue = playerTwoSelect.value;

    for (const option of playerOneSelect.options) {
        option.disabled = option.value === playerTwoValue && option.value !== "";
    }
    
    for (const option of playerTwoSelect.options) {
        option.disabled = option.value === playerOneValue && option.value !== "";
    }
}

function syncCareerResumeRows(){
    const careerLists = document.querySelectorAll(
        ".career-accomplishments"
    );

    if(careerLists.length !== 2){
        return;
    }

    const playerOneRows = careerLists[0].querySelectorAll("li");
    const playerTwoRows = careerLists[1].querySelectorAll("li");

    const allRows = [...playerOneRows, ...playerTwoRows];

    allRows.forEach(function(row){
        row.style.height = "";
    });

    /* Let the rows size naturally on mobile. */
    if(window.innerWidth <= 768){
        return;
    }

    const numberOfRows = Math.min(
        playerOneRows.length,
        playerTwoRows.length
    );

    for(let index = 0; index < numberOfRows; index++){
        const playerOneHeight = playerOneRows[index].offsetHeight;
        const playerTwoHeight = playerTwoRows[index].offsetHeight;

        const matchingHeight = Math.max(
            playerOneHeight,
            playerTwoHeight
        );

        playerOneRows[index].style.height = `${matchingHeight}px`;
        playerTwoRows[index].style.height = `${matchingHeight}px`;
    }
}

